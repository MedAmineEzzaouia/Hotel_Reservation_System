package hotel;

/**
 * Enum representing the available room categories within the hotel.
 * <p>
 * The three supported categories are:
 * <ul>
 *     <li>STANDARD – base level accommodation.</li>
 *     <li>JUNIOR – mid‑tier suite offering additional amenities.</li>
 *     <li>SUITE – highest tier accommodation with premium services.</li>
 * </ul>
 */
public enum RoomType {
    STANDARD,
    JUNIOR,
    SUITE
}